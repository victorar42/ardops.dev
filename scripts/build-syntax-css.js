#!/usr/bin/env node
/**
 * scripts/build-syntax-css.js
 *
 * Generates assets/css/syntax.css from the fixed palette below.
 *
 * Contract: specs/016-syntax-highlighting/contracts/syntax-css-budget.md
 *
 * Modes:
 *   node scripts/build-syntax-css.js           — write the file
 *   node scripts/build-syntax-css.js --check   — exit 0 if disk matches; 2 otherwise
 */

'use strict';

const fs = require('fs');
const path = require('path');
const zlib = require('zlib');

const OUT_PATH = path.resolve(__dirname, '..', 'assets', 'css', 'syntax.css');
const THEME_ID = 'github-dark-default';
const LANGUAGE_COUNT = 21;
const BUDGET_GZIP_BYTES = 5120;

// Palette — verified ≥4.5:1 contrast against #111827 (--bg-secondary).
// See research.md (R-03) and contracts/syntax-css-budget.md.
const TOKEN_COLORS = Object.freeze({
  'tok-keyword':           '#ff7b72',
  'tok-string':            '#a5d6ff',
  'tok-string-expression': '#a5d6ff',
  'tok-comment':           '#9ea9bd',
  'tok-function':          '#d2a8ff',
  'tok-constant':          '#79c0ff',
  'tok-parameter':         '#ffa657',
  'tok-punctuation':       '#c9d1d9',
  'tok-link':              '#a5d6ff',
  'tok-deleted':           '#ffa198',
  'tok-inserted':          '#aff5b4',
});

function buildCss() {
  const header = [
    '/* Generated by scripts/build-syntax-css.js',
    ` * Theme: ${THEME_ID}`,
    ` * Languages: ${LANGUAGE_COUNT}`,
    ' * Do not edit by hand — run `node scripts/build-syntax-css.js` to regenerate.',
    ' */',
  ].join('\n');

  const container = [
    'pre.shiki{background:var(--bg-secondary);color:var(--text-primary);padding:1rem 1.25rem;border-radius:6px;overflow-x:auto;font-family:var(--font-mono,ui-monospace,SFMono-Regular,Menlo,monospace);font-size:.9375rem;line-height:1.55;tab-size:2}',
    'pre.shiki code{background:transparent;padding:0;font:inherit;color:inherit;display:block;white-space:pre}',
    'pre.shiki .line{display:block;min-height:1.55em}',
  ].join('\n');

  const tokenRules = Object.entries(TOKEN_COLORS)
    .map(([cls, color]) => `.${cls}{color:${color}}`)
    .join('\n');

  const italic = '.tok-comment{font-style:italic}';

  return [header, '', container, '', tokenRules, italic, ''].join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const isCheck = args.includes('--check');

  const generated = buildCss();

  if (isCheck) {
    let onDisk;
    try {
      onDisk = fs.readFileSync(OUT_PATH, 'utf8');
    } catch (err) {
      process.stderr.write(`[build-syntax-css] missing: ${OUT_PATH}\n`);
      process.exit(2);
    }
    if (onDisk !== generated) {
      process.stderr.write(`[build-syntax-css] drift: ${OUT_PATH}\n`);
      process.exit(2);
    }
    process.stdout.write(`[build-syntax-css] ✓ ${OUT_PATH} matches generator\n`);
    process.exit(0);
  }

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, generated, 'utf8');

  const raw = Buffer.byteLength(generated, 'utf8');
  const gz = zlib.gzipSync(generated, { level: 9 }).length;

  process.stdout.write(`[build-syntax-css] wrote ${OUT_PATH}\n`);
  process.stdout.write(`[build-syntax-css]   raw=${raw} B  gzip=${gz} B  budget=${BUDGET_GZIP_BYTES} B\n`);

  if (gz > BUDGET_GZIP_BYTES) {
    process.stderr.write(`[build-syntax-css] ✗ over budget (${gz} > ${BUDGET_GZIP_BYTES})\n`);
    process.exit(1);
  }
  process.stdout.write('[build-syntax-css] ✓ under budget\n');
}

if (require.main === module) {
  main();
}

module.exports = { buildCss, OUT_PATH, BUDGET_GZIP_BYTES, TOKEN_COLORS };
